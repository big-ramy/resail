/**
 * تحويل كائن ملف (File) إلى سلسلة Base64.
 * @param {File} file الملف المراد تحويله.
 * @returns {Promise<string>} وعد (Promise) يحل بسلسلة Base64.
 */
function fileToBase64(file) {
    return new Promise((resolve, reject) => {
        const reader = new FileReader();
        reader.readAsDataURL(file);
        reader.onload = () => resolve(reader.result.split(',')[1]);
        reader.onerror = error => reject(error);
    });
}

/**
 * التحقق من صحة تنسيق عنوان البريد الإلكتروني.
 * @param {string} email سلسلة البريد الإلكتروني للتحقق.
 * @returns {boolean} صحيح إذا كان البريد الإلكتروني صالحاً، خطأ بخلاف ذلك.
 */
function validateEmail(email) {
    const re = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    return re.test(email);
}

