/**
 * دالة مساعدة لإزالة إدخال حقل فردي (مثل الخبرة، التعليم).
 * تضمن بقاء حقل واحد على الأقل من نوعه.
 * @param {HTMLElement} button عنصر زر "إزالة".
 */
function removeField(button) {
    const entry = button.parentElement;
    const parentContainer = entry.parentElement;

    let minEntries = 1; // الحد الأدنى الافتراضي لعدد الإدخالات لكل قسم
    const entries = parentContainer.querySelectorAll(`.${entry.className}`);

    if (entries.length <= minEntries) {
        alert(translations[currentLang]['You must have at least one field in this section.']);
        return;
    }

    parentContainer.removeChild(entry);
    generateCV();
    updateProgress();
}

