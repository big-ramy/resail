/**
 * دالة رئيسية للتحكم في رؤية الصفحات.
 * تخفي جميع أقسام الصفحات ثم تظهر القسم المحدد.
 * @param {string} pageId معرف (ID) قسم الصفحة المراد إظهاره.
 */
function showPage(pageId) {
    const pages = document.querySelectorAll('.page-section');
    pages.forEach(page => {
        page.classList.remove('active-page');
    });

    const targetPage = document.getElementById(pageId);
    if (targetPage) {
        targetPage.classList.add('active-page');
        window.scrollTo(0, 0); // التمرير لأعلى الصفحة لتحسين تجربة المستخدم
        updatePageContentLanguage(); // تحديث نصوص الصفحة للغة الجديدة

        // منطق خاص لبعض الصفحات عند إظهارها
        if (pageId === 'cv-data-entry-page') {
            // إخفاء الهيدر الرئيسي وزر البدء عند الانتقال إلى صفحة إدخال البيانات
            const landingPageHeader = document.getElementById('landing-page-header');
            const startCvButton = document.getElementById('start-cv-button');
            if (landingPageHeader) landingPageHeader.style.display = 'none';
            if (startCvButton) startCvButton.style.display = 'none';

            populateWithTestData(); // ملء حقول البيانات ببيانات اختبارية (للتجربة/التطوير)
            generateCV(); // إنشاء معاينة أولية للسيرة الذاتية (ستكون مخفية)
            updateProgress(); // تحديث شريط التقدم
        } else if (pageId === 'cv-template-selection-page') {
            // إعادة إنشاء السيرة الذاتية لمعاينة اختيار القالب (ستكون مخفية)
            generateCV();
            updateProgress();
        } else if (pageId === 'cv-preview-page') {
            // عند عرض صفحة المعاينة، لا نعرض السيرة الذاتية على الشاشة
            // generateCV() تقوم بتحديث cvContainer المخفي
            const cvPreviewArea = document.getElementById('cv-preview-area');
            if (cvPreviewArea) {
                // إخفاء منطقة المعاينة تماماً، حيث لا نريد عرض السيرة الذاتية على الشاشة
                cvPreviewArea.style.display = 'none';
            }
            // يمكن عرض رسالة للمستخدم هنا إذا أردت
            const previewResultDiv = document.getElementById('cv-preview-result');
            if (previewResultDiv) {
                previewResultDiv.style.color = "blue";
                previewResultDiv.textContent = currentLang === 'ar' ?
                    'السيرة الذاتية جاهزة للتنزيل أو الدفع.' :
                    'CV is ready for download or payment.';
                setTimeout(() => {
                    previewResultDiv.textContent = '';
                }, 3000);
            }
        } else if (pageId === 'landing-page') {
            // إظهار الهيدر الرئيسي وزر البدء عند العودة للصفحة الرئيسية
            const landingPageHeader = document.getElementById('landing-page-header');
            const startCvButton = document.getElementById('start-cv-button');
            if (landingPageHeader) landingPageHeader.style.display = 'flex'; // أو block حسب تصميمك
            if (startCvButton) startCvButton.style.display = 'block';
        }
    }
}

