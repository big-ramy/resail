        cvVisibility: cvContainer.style.visibility || '', // حفظ حالة الـ visibility
        cvWordSpacing: cvContainer.style.wordSpacing || '', // حفظ تباعد الكلمات

        // حفظ خصائص الآباء التي قد تتأثر مؤقتًا
